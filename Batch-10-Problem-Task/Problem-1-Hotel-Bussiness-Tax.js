function calculateTax(income, expenses) {
    if(0>income || 0>expenses || expenses>income ) {
        return 'Invalid Input'
    }

    if(income>=expenses){
        let incomeExpensesDiff=income-expenses
        let calculatedTax=incomeExpensesDiff* 0.20
        return calculatedTax
    }
   

}



