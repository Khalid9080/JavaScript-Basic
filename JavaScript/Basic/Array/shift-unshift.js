

/**
 *  Shift() --> Array er first index theke element ber kore.
 * 
 */

const friends=['khalid','sadi','asif','sazim','dip'];
console.log(friends);
friends.shift();
console.log(friends);


/**
 *  unshift() --> Array er first index theke element add kore.
 * 
 */

const shotruz=['khalid','sadi','asif','sazim','dip'];
console.log(shotruz);
shotruz.unshift('Shotruzz');
console.log(shotruz);